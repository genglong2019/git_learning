package cn.zhaolong.checking.manager.entity;

public class Satifies {
    private  int satifiyid;
    private  String sfhead;
    private  String sftype;

    public void setSatifiyid(int satifiyid) {
        this.satifiyid = satifiyid;
    }

    public void setSfhead(String sfhead) {
        this.sfhead = sfhead;
    }

    public void setSftype(String sftype) {
        this.sftype = sftype;
    }

    public int getSatifiyid() {
        return satifiyid;
    }

    public String getSfhead() {
        return sfhead;
    }

    public String getSftype() {
        return sftype;
    }
}
