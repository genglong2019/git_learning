package cn.zhaolong.checking.manager.entity;

public class SDate {
    private int monthid;
    private  int scoress;
    private String sdateing;

    public void setMonthid(int monthid) {
        this.monthid = monthid;
    }

    public void setScoress(int scoress) {
        this.scoress = scoress;
    }

    public void setSdateing(String sdateing) {
        this.sdateing = sdateing;
    }

    public int getMonthid() {
        return monthid;
    }

    public int getScoress() {
        return scoress;
    }

    public String getSdateing() {
        return sdateing;
    }
}
